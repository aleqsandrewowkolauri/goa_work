# ტექსტური ტიპები (Text Types)
# გამოიყენება ტექსტის შესანახად
string1 = "გამარჯობა"  # სტრიქონი (str)
string2 = "პითონი"

# რიცხვითი ტიპები (Numeric Types)
# გამოიყენება რიცხვების შესანახად
integer1 = 42  # მთელი რიცხვი (int)
float1 = 3.14  # ათწილადი რიცხვი (float)
complex1 = 2 + 3j  # კომპლექსური რიცხვი (complex)

# ლოგიკური ტიპები (Boolean Types)
# გამოიყენება ჭეშმარიტების ან მცდარობის შესანიშვნად
is_true = True  # ლოგიკური მნიშვნელობა (bool)
is_false = False

# მონაცემთა კოლექციები (Collection Types)
# გამოიყენება მონაცემთა ჯგუფების შესანახად
my_list = [1, 2, 3]  # სია (list)
my_tuple = (4, 5, 6)  # ტუპლი (tuple)
my_dict = {"სახელი": "ანა", "ასაკი": 25}  # ლექსიკონი (dict)
my_set = {1, 2, 3}  # მრავალკრებული (set)

# დაბეჭდეთ ყველა ცვლადი
print("სტრიქონი:", string1, string2)
print("რიცხვები:", integer1, float1, complex1)
print("ლოგიკური მნიშვნელობები:", is_true, is_false)
print("კოლექციები:", my_list, my_tuple, my_dict, my_set)
