# 4) მომხმარებელს სახელი და გვარი
name = input("შეიყვანეთ სახელი: ")  # მიიღება სახელი
surname = input("შეიყვანეთ გვარი: ")  # მიიღება გვარი
print(f"გამარჯობა {name} {surname}.")  # დაბეჭდეთ გამარჯობა

# 5) 5 ცვლადი და მათემატიკური ოპერაციები
num1 = 10  # პირველი რიცხვი
num2 = 5   # მეორე რიცხვი
num3 = 2   # მესამე რიცხვი
num4 = 3   # მეოთხე რიცხვი
num5 = 8   # მეხუთე რიცხვი

# სხვადასხვა მათემატიკური ოპერაციები
print((num1 + num2) / num3 - num4 * num5 + num1 ** num3 % num2)  # სირთულის ოპერაცია
print(num1 + num2 - num3 * num4 / num5)  # ჯამი და გამოკლება
print(num1 - num2 + num3 ** num4 % num5)  # გამოკლება და ამოწერა
print(num1 * num2 + num3 - num4 / num5)  # გამრავლება და გაყოფა
print(num1 / num2 * num3 - num4 + num5)  # გაყოფა და გამრავლება
print(num1 ** num2 + num3 * num4 - num5)  # თუნდაც ძალა და გამრავლება
print(num1 + num2 * num3 % num4 - num5)  # სიჭარბე და მოცულობა
print(num1 * (num2 + num3) / num4 - num5)  # გაყოფა და გამრავლება
print((num1 - num2) ** num3 * num4 % num5)  # გამოკლება და ძალა
print(num1 % num2 + num3 ** num4 - num5)  # ნაშთი და ძალა
